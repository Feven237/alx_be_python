#!/usr/bin/python3

# Prompt the user for the weather condition
weather = input("What's the weather like today? (sunny/rainy/cold): ")

# Provide advice based on the weather condition
if weather == "sunny":
    print("It's a nice day! Don't forget your sunglasses.")
elif weather == "rainy":
    print("Don't forget your umbrella!")
elif weather == "cold":
    print("Make sure to wear a jacket.")
else:
    print("Invalid input. Please enter sunny, rainy, or cold.")





      
  
